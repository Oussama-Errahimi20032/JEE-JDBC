    void loadServices() {
        for (Service s : ss.findAll()) {
            listeservice.addItem(s);
        }
    }

    void load() {
        model.setRowCount(0);
        for (Client c : cs.findAll()) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNom(),
                c.getPrenom(),
                c.getDate(),
                c.getService()
            });
        }
    }

