    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
//        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        id = Integer.parseInt(model.getValueAt(listeclients.getSelectedRow(), 0).toString());
        c = cs.findById(id);
        nm = nom.getText();
        pm = prenom.getText();
        s = (Service) listeservice.getSelectedItem();
        num = s.getId();
        de = (Date) dateTxt.getDate();
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiement modifiè ce client?" );
        if (reponse == 0) {
            if (cs.update(cs.findById(id))) {
                JOptionPane.showMessageDialog(this,"Bien modifié" );
                load();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

